    public function contact_store(Request $request)
    {
        $request->validate([
            'name' => 'required|min:2|regex:/^[a-zA-Z\s]+$/', // Name should be at least 2 characters long and only alphabetic characters
            'email' => 'required|email', // Valid email format
            'phone' => 'required|numeric|min:10', // Phone should be numeric and at least 10 digits
            'message' => 'required|min:10', // Message should be at least 10 characters long
        ]);

        Contact::create([
            'name' => $request->name,
            'email' => $request->email,
            'phone' => $request->phone,
            'message' => $request->message,
        ]);

        return redirect()->back()->with('success', 'Your message has been sent successfully!');
    }
